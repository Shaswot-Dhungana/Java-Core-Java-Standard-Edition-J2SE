public class Main{
    public static void main(String[] args){

       

        int a = 10, b = 10;

 // Increment Operator

        System.out.println(a++);
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a);

        

        // Decrement Operator

        System.out.println(b--);
        System.out.println(b);
        System.out.println(--b);
        System.out.println(b);

    }
}